    warning('Unrecognized matrix size');
end

if sum(isinf(M),'all')
    warning("Eulerian Transformation is Infinite - Numerical Precision Insufficient")
end

if ~isreal(M)
    warning("Eulerian Transformation is imaginary - Numerical Precision Insufficient")
end

